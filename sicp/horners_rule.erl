-module(horners_rule).
-author("nickolaswood").

%%%=============================================================================
%%% Export and Defs
%%%=============================================================================

%% External API
-export([
    calculate/2
]).

%%%=============================================================================
%%% API
%%%=============================================================================

calculate(X, CoeffSeq) ->
    lists:foldr(
        fun(ThisCoeff, Acc) ->
            ThisCoeff + (X * Acc)
        end,
        0,
        CoeffSeq
    ).

